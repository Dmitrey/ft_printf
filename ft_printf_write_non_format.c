#include "ft_printf.h"

void	ft_printf_percent(char const *f, t_arg *s)
{
	while (f[s->l2] == '%' && f[s->l2 + 1] == '%')
	{
		write(1, &f[s->l2], 1);
		s->re++;
		s->l2++;
	}
	return ;
}
